namespace Create_your_Adventure.Source.Engine.Input
{
    /// <summary>
    /// Defines mouse button identifiers.
    /// Covers standard buttons (left, right, middle) and additional side buttons commonly found on gaming mice.
    /// </summary>
    public enum MouseButton
    {
        /// <summary>Primary mouse button (typically left-click).</summary>
        Left,

        /// <summary>Secondary mouse button (typically right-click for context menus).</summary>
        Right,

        /// <summary>Middle mouse button (scroll wheel click).</summary>
        Middle,

        /// <summary>Additional side button 4 (often used as "back" in browsers).</summary>
        Button4,

        /// <summary>Additional side button 5 (often used as "forward" in browsers).</summary>
        Button5
    }
}